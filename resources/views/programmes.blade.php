                <p>
                <h2 class="text-xl font-bold">Canevas des formations</h2><br>
                <strong>L’Ecole Supérieure de Technologie du Littoral</strong> forme des Techniciens supérieurs susceptibles de remplir le rôle de cadre intermédiaire afin de répondre aux besoins des entreprises de la place.
                <br><br>
                En entreprise, les cadres intermédiaires occupent des fonctions polyvalentes qui incluent la conception, l'installation, la configuration, la maintenance et la supervision des unités de production.
                <br><br>
                Ainsi la formation des étudiants doit absolument conserver un caractère général pour leur permettre de poursuivre d'autres études ou de s'insérer dans le monde du travail et de s'adapter tout au long de leur carrière aux différentes missions qui leurs seront confiées.
                <br><br>
                Pour cela, les unités d'enseignement sont établies suivant deux axes qui visent respectivement à consolider les bases théoriques des étudiants et développer les compétences pratiques en entreprise.
                </p>
